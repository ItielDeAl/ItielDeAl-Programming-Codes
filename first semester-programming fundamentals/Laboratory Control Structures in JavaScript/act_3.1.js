//Determinar si un numero es positivo negativo o cero
let numero = prompt("Ingrese el numero") ; 

if (numero>0){      //si el numero es mayor a 0 sera positivo
    console.log("El numero es positivo");  // si no lo es, no aparecera nada  
}else if(numero<0){
    console.log("El numero es negativo");
    
}else {
    console.log("El numero es igual a 0");
    
}
